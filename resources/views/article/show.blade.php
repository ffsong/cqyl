                    <div class="xinw_t">


                        <p class="MsoNormal">
                    <span style="mso-bidi-font-size:10.5pt;font-family:&quot;微软雅黑&quot;,&quot;sans-serif&quot;;
                  color:#999999">日期：<span lang="EN-US">2018-10-08 </span>来源：省应急管理厅</span></p>
                        <p class="MsoNormal" style="line-height:22.5pt;mso-pagination:widow-orphan">
                            <span style="font-size:14px;"><span style="font-family: 宋体; color: rgb(51, 51, 51);">近年来<span lang="EN-US">,</span>宁波市坚决贯彻城市安全发展战略，突出提升安全生产&ldquo;法治化、标准化、社会化、智能化&rdquo;建设水平，着力破解危险化学品安全综合治理难题，多项危化安全监管工作走在全国、全省前列。宁波市化工行业产值占全省<span lang="EN-US">40%</span>左右，在全国化工行业安全生产形势严峻的新形势下，近三年来，该市危化领域事故始终保持低位运行<span lang="EN-US">,</span>且未发生有较大社会影响的事故，确保了宁波市危险化学品领域安全生产形势的稳定。</span></span></p>
                        <p class="MsoNormal" style="line-height:22.5pt;mso-pagination:widow-orphan">
                            <span style="font-size:14px;"><span style="font-family: 宋体; color: rgb(51, 51, 51);">一、以法治化落实主体责任。针对企业安全生产主体责任落实难的现状，采取法制宣传、规范培训和严格执法三管齐下。一是加大宣传力度。通过集中宣贯、参观交流、微信推送等方式，着重加强对危化品等高危行业关键技术标准的宣贯解读，促进企业依法生产经营。集中开展&ldquo;安全生产月&rdquo;系列活动，依托&ldquo;我与化工零距离&rdquo;公众开放日活动，加强正面引导，不断提高社会公众对危化品的科学认知。二是依法实施培训。重点抓好企业主要负责人、分管负责人和安管员&ldquo;三项岗位&rdquo;人员的安全培训，去年以来，针对主要负责人这一&ldquo;关键少数&rdquo;，精选课程、优选名家，以课前签到、课中点名、课后交流力促<span lang="EN-US">7100</span>多位主要负责人全程参与，带动企业主体责任落实。三是严格监管执法。根据风险程度、安全状况确定<span lang="EN-US">20</span>家危化企业实施市县两级&ldquo;叠加式&rdquo;重点监管。去年以来，累计对<span lang="EN-US">64</span>家危化企业执法检查，直接立案<span lang="EN-US">21</span>起，罚款<span lang="EN-US">82.4</span>万元，实现危化企业事前处罚&ldquo;零&rdquo;突破；暂时停产停业<span lang="EN-US">5</span>家存在重大安全隐患企业，关闭<span lang="EN-US">1</span>家难以整改企业，曝光<span lang="EN-US">1</span>家隐患较多企业；约谈中海石油宁波大榭石化、中金石化以及锦莱化工等企业主要负责人，有力督促、倒逼企业履行法定职责。如，对石化区某韩资企业处罚<span lang="EN-US">11</span>万元，倒逼企业投入安全生产资金<span lang="EN-US">5000</span>万元。同时，积极探索日常监管执法和安全生产许可审批、评审达标等相关联的制度措施，依法督促企业规范管理，自觉落实安全生产主体责任。</span></span></p>
                        <p class="MsoNormal" style="line-height:22.5pt;mso-pagination:widow-orphan">
                            <span style="font-size:14px;"><span style="font-family: 宋体; color: rgb(51, 51, 51);">二、以标准化严格过程管控。完善安全生产标准体系，推动由静态向动态、由结果向过程的质变。一是健全标准化监管流程。出台《宁波市危险化学品安全生产监督检查管理办法》，针对企业类别、风险程度、安全状况等实施分级分类监管，详细指导检查项目、内容、依据、方式、频次等，全面规范执法检查内容和标准。二是规范标准化建设过程。建立专家咨询服务、评审质量抽检、证后退出、动态管理等工作机制，全过程管控危化企业安全生产标准化创建工作质量。截至目前，该市危化品安全标准化达标企业共有<span lang="EN-US">235</span>家，因安全生产条件不符被取消达标资格企业<span lang="EN-US">1</span>家。三是加强化工过程安全管理。选择逸盛石化等<span lang="EN-US">13</span>家企业分两批开展化工过程安全管理试点工作，由国家国际交流合作中心进行专项辅导、培训、交流，逐步推进化工过程安全管理要素落实，全力提升危化企业安全生产管理水平。</span></span></p>
                        <p class="MsoNormal" style="line-height:22.5pt;mso-pagination:widow-orphan">
                            <span style="font-size:14px;"><span style="font-family: 宋体; color: rgb(51, 51, 51);"><span style="background:yellow;mso-highlight:yellow">三、以社会化助推专业服务。充分发挥和调动社会组织的服务功能和社会责任<span lang="EN-US">,</span>为安全生产提供专业化的技术服务、智力支持和社会监督。一是创新安全生产责任险。市安委办与市金融办、市保监局联合印发《关于在高危行业全面实施安全生产责任保险制度的通知》，全面推行安全生产&ldquo;责任保险<span lang="EN-US">+</span>安全服务&rdquo;运营模式，充分发挥保险机制在安全生产工作中的风险控制和社会管理功能，现已实现危化品企业投保&ldquo;全覆盖&rdquo;。将企业安全生产信用作为安全生产责任险、工伤保险费率调整的重要依据，强化市场激励机制作用。二是推广&ldquo;第三方&rdquo;专业技术服务。出台《宁波市危险化学品企业特殊作业引入第三方安全技术服务暂行办法》，引入&ldquo;第三方&rdquo;专业机构对高风险的特殊作业环节进行指导，有效提升安全生产专业化水平。目前，该项服务已在北仑、大榭及石化区三个重点化工园区实现&ldquo;全覆盖&rdquo;。三是搭建危化企业安全生产协作交流平台。石油、有机和精细化工<span lang="EN-US">3</span>个行业小组的<span lang="EN-US">38</span>个成员单位，固定每月开展<span lang="EN-US">1</span>次&ldquo;一对一&rdquo;帮扶活动，每季度开展<span lang="EN-US">1</span>次小组活动，每年组织成员单位间安全&ldquo;互帮、互学、互查&rdquo;活动，大力营造&ldquo;企业帮企业、区域带区域&rdquo;的互助氛围，实现企业间安全生产资源共享，有效提升全市危化企业安全管理整体水平。</span></span></span></p>
                        <p class="MsoNormal" style="line-height:22.5pt;mso-pagination:widow-orphan">
                            <span style="font-size:14px;"><span style="font-family: 宋体; color: rgb(51, 51, 51);">四、以智能化支持精准治理。以&ldquo;政府建设管理、企业申报信息、数据共建共享、部门分工监管&rdquo;智能化平台建设为抓手，探索推动安全生产精准、实时、动态监管。一是实施在线监控预警。目前，该市大榭开发区在全国<span lang="EN-US">16</span>个试点城市中率先建成并投入试运行，初步建成集日常管理、监测监控、预测预警、应急联动等功能为一体的应急指挥和信息平台，进一步提高园区内重大危险源风险管控水平。下一步，将在石化区、北仑区等化工园区推广建设应用。二是实施大数据智能管控。依托危化品大数据平台实行风险公告与安全报备制度，全市危化生产企业按照<span lang="EN-US">2.5</span>万元<span lang="EN-US">/</span>家的补贴标准，每天进行安全风险研判、承诺公告和安全生产状况报备，建立风险隐患管控日志。同时，通过电子屏在企业门口将企业状态、风险等级、安监提示及企业承诺对社会进行公示告知，有效提升企业安全风险管控水平。三是实施重点企业智能化改造。委托中国安科院宁波分院对全市<span lang="EN-US">102</span>家&ldquo;两重点、一重大&rdquo;危化品企业安全监测监控体系和自动化控制系统进行全面诊断，责令不符合国家标准规范要求的<span lang="EN-US">43</span>企业完成整改，推动金海晨光等<span lang="EN-US">11</span>家公司完成自动化系统改造升级，全面提升企业本质安全水平。</span></span></p>
